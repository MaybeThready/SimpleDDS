/**
 *@brief 刷新显示
 * 
 * @param this OLED对象指针
 */
void oled_refresh()
{
    uint8_t i, n;
    for (i = 0; i < 8; i++)
    {
        oled_write_byte(0xb0 + i, OLED_CMD);
        oled_write_byte(0x00, OLED_CMD);
        oled_write_byte(0x10, OLED_CMD);
        DL_GPIO_clearPins(OLED_CS_PORT, OLED_CS_PIN);
        delay_cycles(TIME_MS(5));
        while (DL_SPI_isBusy(OLED_SPI_INST)) { __NOP(); };
        DL_SPI_transmitData8(OLED_SPI_INST, 0x78);
        while (DL_SPI_isBusy(OLED_SPI_INST)) { __NOP(); };
        DL_SPI_transmitData8(OLED_SPI_INST, 0x78);
        while (DL_SPI_isBusy(OLED_SPI_INST)) { __NOP(); };
        DL_SPI_transmitData8(OLED_SPI_INST, 0x40);
        for (n = 0; n < 128; n++)
        {
            while (DL_SPI_isBusy(OLED_SPI_INST)) { __NOP(); };
            DL_SPI_transmitData8(OLED_SPI_INST, oled_gram[n][i]);
        }
        delay_cycles(TIME_MS(5));
        DL_GPIO_setPins(OLED_CS_PORT, OLED_CS_PIN);
    }
}

/**
 *@brief 画点函数
 * 
 * @param x 显示的横坐标，范围0~127
 * @param y 显示的纵坐标，范围0~63
 * @param t 点的状态，1表示点亮，0表示熄灭
 */
void oled_draw_point(uint8_t x, uint8_t y, uint8_t t)
{
    uint8_t i, m, n;
    i = y / 8;
    m = y % 8;
    n = 1 << m;
    if (t)
    {
        oled_gram[x][i] |= n;
    }
    else
    {
        oled_gram[x][i] = ~oled_gram[x][i];
        oled_gram[x][i] |= n;
        oled_gram[x][i] = ~oled_gram[x][i];
    }
}

/**
 *@brief 画线函数
 * 
 * @param x1 
 * @param y1 
 * @param x2 
 * @param y2 
 * @param mode 
 */
void oled_draw_line(uint8_t x1, uint8_t y1, uint8_t x2, uint8_t y2, uint8_t mode)
{
    uint16_t t; 
    int xerr = 0, yerr = 0, delta_x, delta_y, distance;
    int incx, incy, uRow, uCol;
    delta_x = x2 - x1;
    delta_y = y2 - y1;
    uRow = x1;
    uCol = y1;
    if (delta_x > 0)incx = 1;
    else if (delta_x == 0)incx = 0;
    else { incx = -1;delta_x = -delta_x; }
    if (delta_y > 0)incy = 1;
    else if (delta_y == 0)incy = 0;
    else { incy = -1;delta_y = -delta_x; }
    if (delta_x > delta_y)distance = delta_x;
    else distance = delta_y;
    for (t = 0;t < distance + 1;t++)
    {
        oled_draw_point(uRow, uCol, mode);
        xerr += delta_x;
        yerr += delta_y;
        if (xerr > distance)
        {
            xerr -= distance;
            uRow += incx;
        }
        if (yerr > distance)
        {
            yerr -= distance;
            uCol += incy;
        }
    }
}

/**
 *@brief 画圆函数
 * 
 * @param x 
 * @param y 
 * @param r 
 */
void oled_draw_circle(uint8_t x, uint8_t y, uint8_t r)
{
    int a, b, num;
    a = 0;
    b = r;
    while (2 * b * b >= r * r)
    {
        oled_draw_point(x + a, y - b, 1);
        oled_draw_point(x - a, y - b, 1);
        oled_draw_point(x - a, y + b, 1);
        oled_draw_point(x + a, y + b, 1);

        oled_draw_point(x + b, y + a, 1);
        oled_draw_point(x + b, y - a, 1);
        oled_draw_point(x - b, y - a, 1);
        oled_draw_point(x - b, y + a, 1);

        a++;
        num = (a * a + b * b) - r * r;
        if (num > 0)
        {
            b--;
            a--;
        }
    }
}

