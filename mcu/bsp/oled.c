uint8_t oled_gram[130][8];

uint32_t oled_pow(uint8_t m, uint8_t n)
{
    uint32_t result = 1;
    while (n--)
        result *= m;
    return result;
}

/**
 *@brief 向OLED写入一个字节
 * 
 * @param data 要写入的数据
 * @param cmd 指示是命令还是数据，非零表示数据，零表示命令
 */
void oled_write_byte(uint8_t data, uint8_t cmd)
{
    if (cmd)
    {
        DL_GPIO_setPins(OLED_DC_PORT, OLED_DC_PIN);
    }
    else
    {
        DL_GPIO_clearPins(OLED_DC_PORT, OLED_DC_PIN);
    }
    DL_GPIO_clearPins(OLED_CS_PORT, OLED_CS_PIN);
    while (DL_SPI_isBusy(OLED_SPI_INST)) { __NOP(); }
    DL_SPI_transmitData8(OLED_SPI_INST, data);
    while (DL_SPI_isBusy(OLED_SPI_INST)) { __NOP(); }
    DL_GPIO_setPins(OLED_CS_PORT, OLED_CS_PIN);
    DL_GPIO_setPins(OLED_DC_PORT, OLED_DC_PIN);
}

/**
 *@brief 打开OLED显示
 *
 */
void oled_display_on()
{
    oled_write_byte(0X8D, OLED_CMD);
    oled_write_byte(0X14, OLED_CMD);
    oled_write_byte(0XAF, OLED_CMD);
}

/**
 *@brief 关闭OLED显示
 * 
 */
void oled_display_off()
{
    oled_write_byte(0X8D, OLED_CMD);
    oled_write_byte(0X10, OLED_CMD);
    oled_write_byte(0XAE, OLED_CMD);
}

/**
 *@brief 清屏函数
 * 
 */
void oled_clear()
{
    uint8_t i, n;
    for (i = 0; i < 8; i++)
    {
        oled_write_byte(0xb0 + i, OLED_CMD);
        oled_write_byte(0x02, OLED_CMD);
        oled_write_byte(0x10, OLED_CMD);
        for (n = 0; n < 128; n++)
            oled_write_byte(0, OLED_DATA);
    }
    memset(oled_gram, 0, sizeof(oled_gram));
}

/**
 *@brief 填充函数。不建议使用该函数
 *
 * @param x1 
 * @param y1 
 * @param x2 
 * @param y2 
 * @param dot 
 */
void oled_fill(uint8_t x1, uint8_t y1, uint8_t x2, uint8_t y2, uint8_t dot)
{
    uint8_t i, m, n;

    if (y1 % 8 == 0)
        m = y1 / 8;
    else
        m = y1 / 8 + 1;
    if (y2 % 8 == 0)
        n = y2 / 8;
    else
        n = y2 / 8 + 1;
    for (i = x1; i <= x2; i++)
    {
        for (uint8_t j = m; j <= n; j++)
        {
            if (dot)
                oled_gram[i][j - 1] |= (0xFF << (y1 % 8)) & (0xFF >> (8 - y2 % 8));
            else
                oled_gram[i][j - 1] &= ~((0xFF << (y1 % 8)) & (0xFF >> (8 - y2 % 8)));
        }
    }
}

/**
 *@brief 显示一个字符
 * 
 * @param x 显示的横坐标，范围0~127
 * @param y 显示的纵坐标，范围0~7
 * @param chr 要显示的字符
 */
void oled_show_char(uint8_t x, uint8_t y, char chr)
{
    unsigned char c = 0, i = 0;
    c = chr - ' ';
    if (x > MAX_COLUMN - 1)
    {
        x = 0;
        y = y + 2;
    }
    if (OLED_SIZE == 16)
    {
        oled_set_pos(x, y);
        for (i = 0; i < 8; i++)
            oled_write_byte(F8X16[c * 16 + i], OLED_DATA);
        oled_set_pos(x, y + 1);
        for (i = 0; i < 8; i++)
            oled_write_byte(F8X16[c * 16 + i + 8], OLED_DATA);
    }
    else
    {
        oled_set_pos(x, y + 1);
        for (i = 0; i < 6; i++)
            oled_write_byte(F6x8[c][i], OLED_DATA);
    }
}

/**
 *@brief 显示数字
 * 
 * @param x 显示的横坐标，范围0~127
 * @param y 显示的纵坐标，范围0~7
 * @param num 要显示的数字
 * @param len 数字的位数
 * @param size2 字体大小
 */
void oled_show_num(uint8_t x, uint8_t y, uint32_t num, uint8_t len, uint8_t size2)
{
    uint8_t t, temp;
    uint8_t enshow = 0;
    for (t = 0; t < len; t++)
    {
        temp = (num / oled_pow(10, len - t - 1)) % 10;
        if (enshow == 0 && t < (len - 1))
        {
            if (temp == 0)
            {
                oled_show_char(x + (size2 / 2) * t, y, ' ');
                continue;
            }
            else
                enshow = 1;
        }
        oled_show_char(x + (size2 / 2) * t, y, temp + '0');
    }
}

/**
 *@brief 显示有符号数字
 * 
 * @param x 显示的横坐标，范围0~127
 * @param y 显示的纵坐标，范围0~7
 * @param num 要显示的有符号数字
 * @param len 数字的位数
 * @param size2 字体大小
 */
void oled_show_signed_num(uint8_t x, uint8_t y, int32_t num, uint8_t len, uint8_t size2)
{
    uint8_t t, temp;
    uint8_t enshow = 0;
    uint8_t isNegative = 0;

    // 判断是否为负数
    if (num < 0)
    {
        isNegative = 1;
        num = -num;  // 转换为正数以便显示
    }

    // 显示负号
    if (isNegative)
    {
        oled_show_char(x, y, '-');
        x += size2 / 2;  // 更新显示位置
        len--;  // 减少一位用于显示负号
    }

    for (t = 0; t < len; t++)
    {
        temp = (num / oled_pow(10, len - t - 1)) % 10;
        if (enshow == 0 && t < (len - 1))
        {
            if (temp == 0)
            {
                oled_show_char(x + (size2 / 2) * t, y, ' ');
                continue;
            }
            else
                enshow = 1;
        }
        oled_show_char(x + (size2 / 2) * t, y, temp + '0');
    }
}

/**
 *@brief 显示字符串
 * 
 * @param x 显示的横坐标，范围0~127
 * @param y 显示的纵坐标，范围0~7
 * @param p 要显示的字符串
 */
void oled_show_string(uint8_t x, uint8_t y, char* p)
{
    unsigned char j = 0;
    while (p[j] != '\0')
    {
        oled_show_char(x, y, p[j]);
        x += 8;
        if (x > 120)
        {
            x = 0;
            y += 2;
        }
        j++;
    }
}

/**
 *@brief 设置坐标
 * 
 * @param this OLED对象指针
 * @param x 显示的横坐标，范围0~127
 * @param y 显示的纵坐标，范围0~7
 */
void oled_set_pos(uint8_t x, uint8_t y)
{
    oled_write_byte(0xb0 + y + YOFFSET, OLED_CMD);
    oled_write_byte(((x & 0xf0) >> 4) | 0x10, OLED_CMD);
    oled_write_byte((x & 0x0f) | 0x01, OLED_CMD);
}

