/**
 *@brief 显示BMP图片
 * 
 * @param x0 显示的起始横坐标，范围0~127
 * @param y0 显示的起始纵坐标，范围0~7
 * @param x1 显示的结束横坐标，范围0~127
 * @param y1 显示的结束纵坐标，范围0~7
 * @param BMP BMP图片数据数组
 */
void oled_draw_bmp(uint8_t x0, uint8_t y0, uint8_t x1, uint8_t y1, const uint8_t BMP[])
{
    uint16_t j = 0;
    uint8_t x, y;

    if (y1 % 8 == 0)
        y = y1 / 8;
    else
        y = y1 / 8 + 1;
    for (y = y0; y < y1; y++)
    {
        oled_set_pos(x0, y + (!YOFFSET));
        for (x = x0; x < x1; x++)
        {
            oled_write_byte(BMP[j++], OLED_DATA);
        }
    }
}

